package com.marilyzj.model;

public class OrderRepair {
	private String orderrepairId;
	private String orderId;
	private String repairItemsId;

	public String getOrderrepairId() {
		return orderrepairId;
	}

	public void setOrderrepairId(String orderrepairId) {
		this.orderrepairId = orderrepairId;
	}

	public String getOrderId() {
		return orderId;
	}

	public void setOrderId(String orderId) {
		this.orderId = orderId;
	}

	public String getRepairItemsId() {
		return repairItemsId;
	}

	public void setRepairItemsId(String repairItemsId) {
		this.repairItemsId = repairItemsId;
	}

}
