package com.marilyzj.model.jsonbean;

public class Userdata {
	private int amount;

	private int tax;

	private int total;

	private String name;

	public void setAmount(int amount) {
		this.amount = amount;
	}

	public int getAmount() {
		return this.amount;
	}

	public void setTax(int tax) {
		this.tax = tax;
	}

	public int getTax() {
		return this.tax;
	}

	public void setTotal(int total) {
		this.total = total;
	}

	public int getTotal() {
		return this.total;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getName() {
		return this.name;
	}

}
